/*
ASSIGNMENT OPERATORS
*/


let r = 5;
// our equal sign is the assignment.
console.log(5);


// ADDITION ASSIGNMENT

r += 1; // r = r + 1 or, 5 + 1 in this case.

console.log(r);


//SUBTRACTION ASSIGNMENT

r -= 2; // r = r -2 or, 6 - 2 in this case.

console.log(r);

//MULTIPLICATION OPERATORS
r *= 2; // r = r * 2, or in our case 4 * 2.

console.log(r);

// DIVISION OPERATORS

r /= 1; // r = r / 1 or in our case 8 / 1 = 8

console.log(r);

// EXPONENTIAL ASSIGNMENT

r **= 2; // r = r * r, or in our case 8 * 8 
console.log(r);

// MODULUS or REMAINDER ASSIGNMENT

r %= 1; // r = r % 1
console.log(r);

let y = 5;
console.log(y);

y %= 1;
console.log(y);